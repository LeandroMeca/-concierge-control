    public void setLocalAcess(String localAcess) {
        this.localAcess = localAcess;
    }

    public String getLiberator() {
        return liberator;
    }

    public void setLiberator(String liberator) {
        this.liberator = liberator;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getBarCode() {
        return barCode;
    }

    public void setBarCode(String barCode) {
        this.barCode = barCode;
    }
    
    
    
    
